#Calcular cociente y residuo de una division

print('Calcular cociente y residuo de una division')
#Entrada de datos
a = int(input('Ingrese el dividendo: '))
b = int(input('Ingrese el divisor: '))
#Operacion
cociente = a // b
residuo = a % b
#salida de datos
print('El cociente de la division es: ', cociente)
print('El residuo de la division es: ', residuo)
