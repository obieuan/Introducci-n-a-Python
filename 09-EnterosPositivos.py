#Programa que suma los primeros N numeros enteros positivos que ingresa el usuario
print('Suma de N numeros enteros positivos')
#Entrada de datos
n = int(input('Ingrese el numero de numeros a sumar: '))
#Operacion
suma = (n * (n + 1)) / 2
#salida de datos
print('La suma de los numeros es: ', suma)

