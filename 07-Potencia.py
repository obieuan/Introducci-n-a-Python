#Programa que calcula la potencia de un numero
print('Programa que calcula la potencia de un numero')
#Entrada de datos
a = int(input('Ingrese el numero: '))
b = int(input('Ingrese la potencia: '))
#Operacion
potencia = a ** b
#salida de datos
print('La potencia de ', a, ' elevado a la ', b, ' es: ', potencia)
